c = [[int(bool(a or b)) for a in range(-1,2)] for b in range(-1,2)]
for _ in range(int(__import__("sys").argv[1])):
    d = [[0 for _ in range(len(c)*3)] for _ in range(len(c)*3)]
    for e, f in enumerate(c):
        for g, h in enumerate(f):
            for i in range(-1, 2):
                for j in range(-1, 2): d[(3*e)+1+j][(3*g)+1+i] = int(bool((h and (i or j))))
    c = d
for e in range(len(c)): print("".join([("#" if i == 1 else " ") for i in c[e]]))

